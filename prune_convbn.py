    #
    pruned_model = ModelPruned(maskbndict=maskmergedict, cfg=pruned_yaml, ch=3).cuda()
    # Compatibility updates
    for m in pruned_model.modules():
        if type(m) in [nn.Hardswish, nn.LeakyReLU, nn.ReLU, nn.ReLU6, nn.SiLU, Detect, Model]:
            m.inplace = True  # pytorch 1.7.0 compatibility
        elif type(m) is Conv:
            m._non_persistent_buffers_set = set()  # pytorch 1.6.0 compatibility

    from_to_map = pruned_model.from_to_map
    pruned_model_state = pruned_model.state_dict()
    assert pruned_model_state.keys() == modelstate.keys()
    # ======================================================================================= #
    changed_state = []
    for ((layername, layer),(pruned_layername, pruned_layer)) in zip(model.named_modules(), pruned_model.named_modules()):
        assert layername == pruned_layername
        if isinstance(layer, nn.Conv2d) and not layername.startswith("model.24") and convname not in ignore_conv_list:
            convname = layername[:-4]+"bn"
            if convname in from_to_map.keys():
                former = from_to_map[convname]
                if isinstance(former, str):
                    out_idx = np.squeeze(np.argwhere(np.asarray(maskmergedict[layername.rsplit(".",1)[0] + ".bn"].cpu().numpy())))
                    # print("out_idx:",out_idx)

                    in_idx = np.squeeze(np.argwhere(np.asarray(maskmergedict[former].cpu().numpy())))

                    w = layer.weight.data[:, in_idx, :, :].clone()
                    # print("layer : ", layername, layer.weight.data.size(), " out_idx:", out_idx, " in_idx:", in_idx, " w:",w.shape)
                    if len(w.shape) == 3:
                        print("need expand")
